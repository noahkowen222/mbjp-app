export function useProgramTrackingCopy<P extends ProgramKey>(program: P) {
  const { language } = useI18n()
  const selectedLanguage: AppLanguage = language in common ? language : 'en'
  const textDir: TextDirection = selectedLanguage === 'en' ? 'ltr' : 'rtl'
  const isRtl = textDir === 'rtl'

  return {
    copy: {
      common: common[selectedLanguage],
      program: programCopy[program][selectedLanguage] as (typeof programCopy)[P][AppLanguage],
    },
    language: selectedLanguage,
    textDir,
    isRtl,
    textAlignClass: isRtl ? 'text-right' : 'text-left',
    arrowClass: isRtl ? 'mr-2 rotate-180' : 'ml-2',
    iconBeforeClass: isRtl ? 'ml-2' : 'mr-2',
  }
}
