export function useAdminManagementCopy(pageKey: ManagementPageKey) {
  const { language } = useI18n()
  const textDir: TextDirection = language === 'en' ? 'ltr' : 'rtl'

  return {
    copy: {
      common: common[language],
      page: pages[pageKey][language],
    },
    language,
    textDir,
    isRtl: textDir === 'rtl',
    textAlignClass: textDir === 'rtl' ? 'text-right' : 'text-left',
    iconBeforeClass: textDir === 'rtl' ? 'ml-2' : 'mr-2',
    arrowClass: textDir === 'rtl' ? 'mr-2 rotate-180' : 'ml-2',
  }
}
